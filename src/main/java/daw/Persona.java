package daw;

import java.time.LocalDate;
import java.util.Objects;

/**
 *
 * @author Dan_n
 */
public abstract class Persona extends Nif{

private String nombre;
private String apellidos;

    public Persona(String nombre, String apellidos, long numero, LocalDate fechaCaducidad) {
        super(numero, fechaCaducidad);
        this.nombre = nombre;
        this.apellidos = apellidos;
    }

    

    public void renovarNIF(LocalDate fechaSolicitud){
        this.renovarDni(fechaSolicitud);
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellidos() {
        return apellidos;
    }

    public void setApellidos(String apellidos) {
        this.apellidos = apellidos;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 97 * hash + Objects.hashCode(this.nombre);
        hash = 97 * hash + Objects.hashCode(this.apellidos);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Persona other = (Persona) obj;
        if (!Objects.equals(this.nombre, other.nombre)) {
            return false;
        }
        return Objects.equals(this.apellidos, other.apellidos);
    }

    @Override
    public String toString() {
        StringBuilder sb = new StringBuilder();
        sb.append("Persona{");
        sb.append("nombre=").append(nombre);
        sb.append(", apellidos=").append(apellidos);
        sb.append('}');
        return sb.toString();
    }



    
}
